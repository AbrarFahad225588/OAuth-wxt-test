async function waitForElement<T extends Element>(
  selector: string,
  timeout = 10000
): Promise<T> { 
  return new Promise((resolve, reject) => {
    const interval = 100;

    let elapsed = 0;

    const timer = setInterval(() => {
      const el = document.querySelector(selector);

      if (el) {
        clearInterval(timer);
        resolve(el);
      }

      elapsed += interval;

      if (elapsed >= timeout) {
        clearInterval(timer);
        reject(
          new Error(
            `Timeout: ${selector}`
          )
        );
      }
    }, interval);
  });
}
